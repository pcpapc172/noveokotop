fun localizeDigits(input: String, languageCode: String): String {
    if (languageCode != "fa" && languageCode != "ar") return input
    val targetDigits = if (languageCode == "fa") {
        charArrayOf('۰', '۱', '۲', '۳', '۴', '۵', '۶', '۷', '۸', '۹')
    } else {
        charArrayOf('٠', '١', '٢', '٣', '٤', '٥', '٦', '٧', '٨', '٩')
    }
    return input.map { char ->
        if (char in '0'..'9') targetDigits[char - '0'] 
        else if (char == ',') {
            if (languageCode == "fa" || languageCode == "ar") '،' else ','
        }
        else char
    }.joinToString("")
}

fun formatMembersCount(count: Int, strings: NoveoStrings): String {
    val localizedCount = localizeDigits(count.toString(), strings.languageCode)
    return strings.membersCountFormat.replace("%d", localizedCount)
}

fun formatMessagesCount(count: Int, strings: NoveoStrings): String {
    val localizedCount = localizeDigits(count.toString(), strings.languageCode)
    val noun = if (count == 1) strings.messagePlaceholder.lowercase() else strings.messages.lowercase()
    return "$localizedCount $noun"
}

fun formatChatType(chatType: String, strings: NoveoStrings): String {
    return when (chatType.lowercase()) {
        "group" -> strings.group
        "channel" -> strings.channel
        "private" -> strings.privateChatType
        else -> strings.unknown
    }
}

internal fun localizeMessagePreview(preview: String, strings: NoveoStrings): String {
    return when (preview) {
        "Sticker" -> strings.sticker
        "Photo" -> "Photo"
        "Video" -> "Video"
        "File" -> "File"
        "Forwarded message" -> strings.forwarded
        "edited" -> strings.edited
        else -> preview
    }
}
